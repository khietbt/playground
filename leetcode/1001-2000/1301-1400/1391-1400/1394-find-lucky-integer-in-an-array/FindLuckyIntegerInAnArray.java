class Solution {
  public int findLucky(int[] arr) {
    var counts = new int[501];

    for (var i : arr) {
      counts[i]++;
    }

    for (var i = 500; i > 0; i--) {
      if (counts[i] == i) {
        return i;
      }
    }

    return -1;
  }
}
